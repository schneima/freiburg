<p>Das Ausbildungssystem unterteilt sich auf drei aufeinanderfolgende Stufen:</p>
<p>
<ul>
    <li>
        <span class="strong">Bongoklasse - Klasse 1+2</span><br>
    Einmal wöchentlich wird während einer Schulstunde getrommelt was das Zeug hält. Es wird gemeinsam spielerisch musiziert und die ersten Erfahrungen mit Rhytmus und Instrumenten gewonnen. Die Trommeln werden kostenfrei zur Verfügung gestellt, die Teilnahme kostet monatlich lediglich 15€ je Kind.
        <br>        <br>
    </li>

    <li>
        <span class="strong">Bläserklasse - Klasse 3+4</span><br>
    Jetzt geht es an die Instrumente! Wiederum einmal wöchentlich findet morgens in der Grundschule das gemeinsame Musizieren in der Bläserklasse statt. So wird früh und vor allem gemeinsam in der Gruppe musiziert, erste Erfolge und Erfahrungen werden bei gemeinsamen Auftritten bei Schulfesten etc. gesammelt. 
	Zudem hat jeder Schüler je nach Zusammensetzung der Gruppe alleine 
oder in Kleingruppen bei einem ebenfalls qualifizierten Lehrer der 
Jugendmusikschule Instrumentalunterricht.<br>
    Die vorherige Teilnahme an der Bongoklasse ist <span class="strong">keine Voraussetzung</span>. Für die Bläserklasse sind keine Vorkenntnisse erforderlich.<br>
Dirigent der Bläserklasse ist <span class="strong">Eckhard Albert</span>.
            <br>        <br></li>

    <li>

        <span class="strong">Jugendkapelle - ab Klasse 5</span><br>
    Nach Beendigung der Grundschule wechseln die Bläserklassenschüler nun 
    gemeinsam und nahtlos in die Jugendkapelle.<br>
    Die Jugendkapelle ist die Vorstufe zum Hauptorchester, der Trachtenkapelle. 
    Das Orchester wird von Konstantin Rinderle geleitet. 
    Hiermit ergibt sich schon früh ein Bezug zur Trachtenkapelle.<br>
    
      Gemeinsam werden interessante Stücke gespielt 
und das Können beim Vorspielnachmittag,  als Eröffnung von 
Konzerten der Trachtenkapelle und weiteren Anlässen unter Beweis 
gestellt.
<p>Das Orchester besteht aus Jugendlichen im Alter von 
10-16 Jahren, die sich durch soziale Aktivitäten (z.B. Ausflüge) auch neben der Musik kennenlernen. </p>
<br>
    </li>
    <li>
        <span class="strong">Eintritt in die Trachtenkapelle</span><br>
   Nachdem Ihr mehrere Jahre in der Jugendkapelle gespielt und 
Erfahrungen gesammelt habt freuen wir uns mit der Trachtenkapelle auf Euch.<br>

Anfangs unter den Fittichen eurer Registerkollegen
und gleichzeitig noch als Unterstützung der Jugendkapelle werdet ihr 
schnell vom Spielen im großen Orchester und dem Drumherum 
begeistert sein. 
    <p></p>
    
   
    
    
    
    </li>
</ul>
    
</p>

