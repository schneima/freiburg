<h1>Jugendkapelle</h1>
<h2>Jugendkapelle unter neuer Leitung</h2>
<P>
    Seit diesem Sommer wird die Jugendkapelle von Eva-Maria Ruf geleitet.<br>
    Der erste Auftritt unter neuer Leitung wird das Jahreskonzert am Samstag, 22. November in der Möhlinhalle sein.
</P>
<p>
    <img src="../images/eva_maria_ruf_thumb.jpg" alt="Eva-Maria Ruf"/>.
</P>
<P>
Eva-Maria ist 1997 geboren und spielt seit acht Jahren Querflöte und wir von Maike Röhm unterrichtet.<br>
Neben Querflöte spielt sie seit sechs Jahren Klavier und nimmt Unterricht bei Hermann Haege.
Eva-Maria nimmt seit 2011 auch Gesangsunterricht bei Gudula Gertzen.</p>
<p>
Sie hat nicht nur eine Instrumentalausbildung, sondern auch eine Ausbildung zur Musikmentorin 2013 beim BDB erfolgreich abgeschlossen.<br>

Ausgezeichnet wurde sie 2012 bei Jugend musiziert mit dem 3. Preis im Landeswettbewerb.
Das JMLA Abzeichen in Gold hat sie im Frühjahr 2014 an der BDB Akademie in Staufen abgelegt.<br>
</P>
<p>

Neben dem Musikverein in Bollschweil hat sie in unterschiedlichen Projekten und Orchestern teilgenommen.
Als Sängerin war sie beim Independent schools choral festival in Washington D.C. dabei.
</P>