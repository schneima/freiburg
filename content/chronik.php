<h3>1963 - 1973</h3>

<strong>17. -19. August 1963</strong><br>
100-j&auml;hriges Musikfest.<br>
Am 18.8. riss ein starker Sturm die Planen vom Dach des Festzeltes los.<br><br>

Zitat Bernhard Mangold:<br>
"Mit H&auml;mmer und N&auml;geln bewaffnet stiegen die Musikanten auf's Dach um die Planen wieder anzunageln. So konnten wir aktiven Musiker den Festakt, der sich im Zelt abspielte auf dem Dach des Festzeltes miterleben. Die T&ouml;ne, die vom Zelt zu uns hinaufstiegen und das Heulen des Sturmes vereinte sich mit den Hammerschl&auml;gen der Musikanten. Deshalb wird uns das 100-j&auml;hrige Musikfest noch lange in Erinnerung bleiben".<br><br>
<strong>20. Juni 1965</strong><br>
Im Gasthaus zum L&ouml;wen wurde der 75. Geburtstag von Oberlehrer Graf und gleichzeitig seine Verabschiedung als Dirigent des Kirchenchores gefeiert.<br><br>
<strong>Dezember 1967 bis Dezember 1968</strong><br>
In dieser Zeit wurden 11 Z&ouml;glinge durch den Berufsmusiker Herrn M&auml;usert vom Stadttheater Freiburg ausgebildet. Am Wei&szlig;en Sonntag 1969 war ihr erster Auftritt. Von diesen 11 Z&ouml;glingen sind heute noch dabei: Meinrad Grammelspacher, Klaus Weber und Max Riesterer. Die Kapelle z&auml;hlte 30 Aktive.<br><br>
<strong>23. M&auml;rz 1969</strong><br>
Hans Disch wird in der Generalversammlung zum 1. Vorstand gew&auml;hlt.<br><br>
<strong>8. M&auml;rz 1970</strong><br>
