Die Breisgauer Baustoffwerke feiern ihr 50-j&auml;hriges Bestehen in der neuen Lagerhalle und laden die gesamte Bev&ouml;lkerung ein. Die Musikkapelle spielt zur Unterhaltung. In seiner Festrede gibt der damalige Chef Artur Koch bekannt, dass die Breisgauer Baustoffwerke dem Musikverein eine Tracht stiften wird. Diese wurde am 17. Januar 1971 am Patrozinium zum ersten mal getragen.<br>
