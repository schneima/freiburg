<a name="18631887"></a>
<h3>1863 - 1887</h3>
  Wir schreiben das Jahr <strong>1863</strong>. Was war das f&uuml;r eine Zeit, was
  geschah damals in Bollschweil und andernorts auf der Welt. Die Menschen mussten 
  noch 14 Jahre warten bis Thomas Alpha Edison 1877 den Zylinderplattenspieler 
  und damit den ersten Tontr&ouml;ger erfand. Wollte man Musik h&ouml;ren, so musste man
  sie entweder selbst machen oder ein Konzert besuchen. Eines war damit sicher, 
  h&ouml;rte man vor 1877 Musik, dann war sie live.<br>
  <br>
  Noch 16 Jahre, von 1863 an gerechnet, mussten die Musikanten in Bollschweil 
  und &uuml;berall auf der Welt bei Kerzenlicht oder im Schein einer anderen Funzel
  nach erl&ouml;schen des Sonnenlichtes proben. Denn erst 1879 meldete der selbe Edison
  das Patent f&uuml;r seine Gl&uuml;hbirne an. In Bollschweil haben die Musiker sicher noch
  sehr viel l&auml;nger im Dunkeln gemunkelt, denn von der Erfindung des elektrische
  Lichtes bis zur Elektrifizierung und elektrischen Beleuchtung Bollschweils war 
  es sicher noch ein langer Weg.<br>
  <br>
  1863 Giuseppe Verdi in Italien, Komponist von Werken wie Maskenball, die Macht 
  des Schicksals, Don Carlos und Othello war auf dem Zenith seiner Schaffenskraft. 
  Wenige Jahre sp&auml;ter(1871) wurde Aida, eines seiner ber&uuml;hmtesten Werke in Kairo
  anl&auml;sslich der Er&ouml;ffnung des Suezkanales uraufgef&uuml;hrt.<br>
  <br>
  1863 starb Franz Gruber. Ein Mann, der nie ber&uuml;hmt wurde obwohl er ein Lied
  geschrieben hat, das jeder kennt und heute in der ganzen Welt popul&auml;r ist. Unz&auml;hlige
  Interpreten (von den Regensburger Domspatzen bis zu Mahelia Jackson) haben es 
  in unterschiedlichen Arrangements gesungen. Es wurde am 24.12.1818 zum ersten 
  mal in der Kirche von Oberndorf aufgef&uuml;hrt. 1863 starb Franz Gruber, der Komponist
  des Liedes "Stille Nacht."<br>
  <br>
  Es war die Epoche des B&uuml;rgerkrieges in den USA, der 1865 mit dem Sieg der industriellen
  Nordstaaten endete. Pr&auml;sidenten Lincoln wurde ermordet. Die Sklaverei wurde
  abgeschafft.<br>
  <br>
  Es war die Epoche in der Zeitgenossen wie Johann Strau&szlig; Sohn Werke wie den Kaiserwalzer,
  Zigeunerbaron, Wiener Blut oder die Fledermaus geschaffen haben.<br>
  <br>
  Bismarck war preu&szlig;ischer Ministerpr&auml;sident.<br>
  <br>
  1863 starb Jakob Grimm der &auml;ltere der beiden Gebr&uuml;der Grimm, die sp&auml;ter durch
  ihre M&auml;rchen weltber&uuml;hmt wurden.<br>
  <br>
  Doch nicht aus den M&auml;rchenb&uuml;chern der Gebr&uuml;der Grimm -liebe LeserInnen- sondern
  aus sicherer m&uuml;ndlicher &Uuml;berlieferung wissen wir, dass es 1863 in Bollschweil
  bereits eine Vereinsmusik gab, die auch au&szlig;erhalb von Bollschweil bei verschiedenen
  Anl&auml;ssen &auml;ffentlich auftrat und die Wurzel des heutigen Musikvereines/Trachtenkapelle
  Bollschweil bildet.<br>
