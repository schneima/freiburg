<a name="19151958"></a>
<h3>1915 - 1958</h3>
Aus dieser Zeit liegt uns keine geschlossene Dokumentation mehr vor.<br>
Die grossen politischen Ereignisse dieser Epoche m&uuml;ssen nicht kommentiert werden. Die Zeit war gekennzeichnet durch die beiden Weltkriege. Aus der Musikszene sollen in diesem Zusammenhang die Comedien Harmonists genannt werden, die man heute wohl als erste "boygroup" der Welt bezeichnen w&uuml;rde. Sie hatten mit ihren A-capella St&uuml;cken riesige Erfolge bis man der Gruppe 1934 jeglichen Auftritt in Deutschland verboten hat.<br><br>

Die herausgerissenen Bl&auml;tter dieses Buches dokumentieren die ganze Sprachlosigkeit dieser Zeit. Selbst ein vermeintlich harmloses Kassen- und Schriftf&uuml;hrerbuch eines Musikvereines musste wohl verstummen.<br><br>

Aus m&uuml;ndlichen &Uuml;berlieferungen und einigen Bilddokumentationen wissen wir, dass es auch in diesen Zeiten einen Musikverein gegeben hat.<br><br>
