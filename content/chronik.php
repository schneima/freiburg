<h1>1888 - 1914</h1>
  Wir schreiben das <strong>Jahr 1888</strong>, das Jahr aus dem uns die erste 
  schriftliche Dokumentation vorliegt. Es handelt sich um ein Kassenbuch, das 
  von Joseph Gutgsell, der von <strong>1889 bis 1899</strong> Dirigent und Vorstand 
  war, gestiftet wurde. Dieses Buch enth&auml;lt die gesamte Dokumentation des Vereines
  bis zum ersten Weltkrieg und wurde vom jeweiligen Vorstand bis zum heutigen 
