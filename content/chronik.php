<h3>2003 bis 2015</h3>


<strong>5. April 2003</strong><br>
Auftaktveranstaltung zum 140-j&auml;hrigen Jubil&auml;um der Trachtenkapelle und dem 25-j&auml;hrigen Bestehen der Jugendmusikschule S&uuml;dlicher Breisgau.<br>
Die jungen MusikerInnen unserer Jugendkapelle und das Ensemble der Jugendmusikschule boten in einer gut besuchten M&ouml;hlinhalle ein Konzert auf hohem Niveau. Eine gut organisierte Veranstaltung die man auch ohne Jubil&uuml;um wieder einmal durchf&ouml;hren k&ouml;nnte.<br><br>
<strong>12. April 2003</strong><br>
Festbankett zum 140-j&auml;hrigen Jubil&auml;um der Trachtenkapelle unter Mitwirkung des Chores Crescendo, der Trachtenkapelle St.Ulrich, des M&auml;nnergesangvereines Eintracht und des Kirchenchores St. Hilarius. Die Vorst&auml;nde und Dirigenten der letzten 30 Jahre waren an diesem Abend eingeladen und bis auf wenige Ausnahmen alle gekommen. Der 1.Vorsitzende Max Riesterer nahm die G&auml;ste mit auf eine Reise durch 140 Jahre Trachtenkapelle Bollschweil.<br>
