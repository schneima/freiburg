Jugendmusiktag in der M&ouml;hlinhalle. Nach einem Workshop f&uuml;r Kinder und Jugendliche gab es ein kleines Konzert der beiden Jugendkapellen aus Bollschweil und Opfingen. Es zeigte sich wieder einmal, dass unser Nachwuchs bei Eckhard Albert in guten H&auml;nden ist.<br>
