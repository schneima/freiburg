Zitat unseres Schriftf&uuml;hrers Meinrad Grammelspacher:<br>
"Wenn der B&uuml;rgermeister heiratet steht das Dorf Kopf". Wir standen nicht Kopf sondern holten unseren B&uuml;rgermeister Josef Schweizer mit seiner Braut vom Elternhaus im Unterdorf ab, begleiteten beide auf Ihrem Weg in die Kirche und umrahmten die Brautmesse.<br><br>
<strong>18.-21. Juni 1993</strong><br>
130-j&auml;hriges Jubil&auml;um im Festzelt bei der M&ouml;hlinhalle. Unter der musikalischen Leitung von Konrad Vonarb und mit Hanspeter Moll als 1. Vorstand wurde dieses Jubil&auml;um gefeiert.<br>
Neben dem Festumzug war auch bei diesem Fest der Auftritt des Jugend- und Blasorchesters Murnau ein besonderer H&ouml;hepunkt.<br><br>
<strong>6. Juni 1996</strong><br>
Erster Musikhock am Rathaus, eingef&uuml;hrt von Hanspeter Moll. Dieser Hock ist seit dort fester Bestandteil unseres Vereinskalenders und eine unverzichtbare Einnahmequelle f&uuml;r den Verein.<br><br>
<strong>13. Juli 1996</strong><br>
Good bye Konny; Abschiedskonzert f&uuml;r Konrad Vonarb. Besondere &Uuml;berraschung f&uuml;r ihn (Oh happy day) - Sch&ouml;n war die Zeit.<br><br>
<strong>15. Dezember 1996</strong><br>
Ein Mann im schwarzen Anzug schreitet durch den Mittelgang der Hilariuskirche in Bollschweil wo ihn die Trachtenkapelle und viele Zuh&ouml;rer mit Spannung erwarten. Deb&uuml;t unseres neuen Dirigenten Friedrich M&uuml;ller. Mit St&uuml;cken wie Bohemian Rhapsody (Freddy Mercury) und Air for winds schlug er eine neue Richtung ein.<br><br>
<strong>22. November 1997</strong><br>
Herbstzeit-Konzertzeit. Unter diesem Motto veranstalteten wir das Herbstkonzert. Zusammen mit unserer Gastkapelle aus H&uuml;gelheim und unserer neu formierten Jugendkapelle boten wir ein tolles Konzert. Mit St&uuml;cken wie Batman und Tirol 1809 wurde dem Publikum ein weites Spektrum an Blasmusik geboten.   <br><br>
<strong>4. April 1998</strong><br>
Zum ersten mal in unserer Original Breisgauer Tracht und leider zum letzten mal unter der Stabf&uuml;hrung von Friedrich M&ouml;ller gab die Trachtenkapelle ihr Fr&uuml;hjahrskonzert. Mit St&uuml;cken wie Moment for Moriccone und einem Orchesterwerk aus der Oper Banditenstreiche verabschiedete sich unser Dirigent vom Publikum der Trachtenkapelle.<br>
<strong><br>
19. April 1998</strong><br>
Generalversammlung: Hanspeter Moll gibt nach fast 10 Jahren das Amt als 1. Vorsitzender ab. Mit Bedauern verabschieden wir uns auch von Friedrich M&ouml;ller, der aus beruflichen Gr&uuml;nden das Badische L&auml;ndle verl&auml;sst.<br>
Max Riesterer wird zum 1.Vorsitzenden gew&auml;hlt und nimmt das Amt an.<br><br>
<strong>5. Juli 1998</strong><br>
Einweihung des neuen Feuerwehrfahrzeuges. Die Trachtenkapelle umrahmt den Festakt und spielt unter der Leitung des Vizedirigenten Markus Weiser zur Unterhaltung.<br><br>
<strong>31. Juli 1998</strong><br>
Einweihung der neuen Ortsdurchfahrt.<br>
Die Kapelle spielt bei dem von der Gemeinde arrangierten Fest.<br>
