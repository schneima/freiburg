h<h1>Bläserklasse</h1>
<h2>...die ersten Schritte</h2>
<p>
Das Projekt Bläserklasse ist eine Kooperation der Marie-Luise-Kaschnitz Grundschule Bollschweil,<br>
dem Musikverein Bollschweil eV. und der Musikschule Markgräflerland e.V.<br> und richtet sich immer an Kinder der Klassenstufen 3 und 4.    
</p>

<p>
Das Projekt umfasst:
</p>
<ul>
    <li>Kennen lernen der Instrumente im Blasorchster</li>
    <li>30 Minuten Einzelunterricht pro Woche bei Instrumentallehrern der Musikschule Markgräflerland e.V.</li>
    <li>Eine wöchentliche "Bläserstunde" während des Schulvormittags, in der die Kinder von Anfang an das Zusammenspiel im Orchester üben.</li>
    <li>Ein Leihinstrument für die Dauer des Bläserklassen-Projektes (2 Jahre) gestellt vom Musikverein Bollschweil</li>
    <li>Derzeit besuchen insgesamt 12<?php //TODO ?> Kinder die Bläserklasse unter der Leitung von Eckhard Albert.</li>
    <li>Die Bläserklasse hat sich als fester Bestandteil des Schullebens etabliert. Ein Auftritt der Bläserklasse gehört bei vielen schulischen Veranstaltungen dazu.</li>
</ul>
<p>
Möglichkeiten in das "Projekt" Bläserklasse einzusteigen besteht immer zu Schuljahresbeginn.<br>
Voraussetzung ist lediglich das Interesse ein Blasinstrument zu erlernen.<br>
Musikalische Vorkenntnisse sind NICHT erforderlich.

Die Bläserklasse freut sich über Zuwachs !<br>
</p>
