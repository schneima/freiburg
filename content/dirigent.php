<br>
<p>
    Seit Januar 2019 wird der Musikverein Bollschweil von Pascal Arets geleitet.
</p>
<p>
Pascal Arets, geboren 1972 in Kerkrade (Niederlande), seine musikalische Ausbildung 
begann er im Alter von 8 Jahren an der Musikschule in Kerkrade (NL). Ab dieser Zeit 
spielte er im örtlichen Blasorchester St. Jozef Kaalheide-Kerkrade und anderen 
Blasorchestern in der Region.
</p>
<p>
Dann folgte ein Studium Horn an der Musikhochschule Maastricht bei Prof. Erich Penzel, 
wo er 1996 mit dem Konzertdiplom abschloss, sowie Dirigieren bei Alex Schillings.
Er arbeitete während seiner Studienzeit zusammen mit Dirigenten wie u.a. Jan Cober 
(Nationales Jugend Blasorchester der Niederlande), Seff Pijpers (Blasorchester der 
Musikhochschule Maastricht, Brassband Limburg), und Maurice Hamers.</p>
<p>
Weiter spielte er als Hornist beim Europäischen Jugend Orchester, beim Schleswig-
Holstein Musik Festival, sowie beim Blechbläser Ensemble Flexible Brass.</p>
<p>
1995 folgte eine Anstellung als Hornist am Pfalztheater in Kaiserslautern, und bald 
darauf am Badischen Staatstheater Karlsruhe. Seit 2000 Anstellung als Hornist beim 
SWR Sinfonie Orchester Baden-Baden und Freiburg, jetzt SWR Symphonie Orchester.
Seit 2008 leitet er außerdem den Musikverein Ballrechten-Dottingen.</p>
<p>
Pascal Arets musizierte mit Orchestern wie dem Rundfunk Sinfonie Orchester Köln 
(WDR), NDR Elbphilharmonie Orchester, Radio Symphony Orchestra Hilversum, Sinfonie 
Orchester des Bayrischen Rundfunks, Staatsoper Stuttgart.</p>
<p>
Konzertreisen führten ihn nach Russland, Japan, Singapur, Indonesien sowie die 
meisten europäischen Länder.
</p>




