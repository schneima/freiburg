<br>
<p>Ab Januar 2019 wird der Musikverein Bollschweil von Pascal Arets geleitet.</p>
<p>
Nachdem Carl-Philipp Rombach seine erfolgreiche Zeit bei uns mit dem Jahreskonzert im November beendet hat freuen wir uns,
Ihnen und Euch unseren neuen Dirigenten vorstellen zu können: Pascal Arets!<br>
Pacal Arets ist selbst Hornist im Symphonieorchester des SWR und u. a. durch seine Tätigkeit beim Musikverein Ballrechten-Dottingen 
(die er ebenfalls weiterführen wird) kein Unbekannter in der Region.
</p>
<p>
    Wir sind überzeugt mit ihm wieder einen passenden Dirigenten gefunden zu haben und freuen uns auf die im Januar beginnende Zusammenarbeit.

<br>In diesem Sinne: Vielen Dank Carl-Philipp Rombach für die letzten 8 Jahre und herzlich Willkommen in Bollschweil Pascal Arets!

</p>




